$.User = class extends $.Users {
    
/*--------------------------------------------------------------------------------------------------
|
| -> Методы
|
|-------------------------------------------------------------------------------------------------*/

// Сохраняет ID последнего сообщения
    setMessageID(message_id, callback) {
        this.userInfo['message_id'] = message_id;
        $.Users.set(this.userInfo, callback);
    }
}

