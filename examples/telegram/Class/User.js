/*--------------------------------------------------------------------------------------------------
|
| -> Конструктор
|
|-------------------------------------------------------------------------------------------------*/

$.User = function(userInfo) {
// Сохраняем информацию о пользователе
    Object.defineProperty(this, '_userInfo', {value: userInfo});
};

//--------------------------------------------------------------------------------------------------

$.User.prototype = {
    
/*--------------------------------------------------------------------------------------------------
|
| -> GET
|
|-------------------------------------------------------------------------------------------------*/

// ID пользователя
    get user_id() {
        return this._userInfo['user_id'];
    },
    
/*--------------------------------------------------------------------------------------------------
|
| -> SET
|
|-------------------------------------------------------------------------------------------------*/

// Сохраняет ID последнего сообщения
    setMessageID: function(message_id, callback) {
        this._userInfo['message_id'] = message_id;
        $.Users.set(this._userInfo, callback);
    }
};

